    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if (flag==false){          
        
            flag=true;
            startTime = System.currentTimeMillis();
            userCounter++;
            jTextArea1.append("Current user "+userCounter+" started \n");
            jButton2.setText("Завершить работу");
            jPanel1.setVisible(true);
        }
        else{
            flag=false;
            finishTime=System.currentTimeMillis();
            workLength=(double)(finishTime-startTime)/1000;
            jTextArea1.append("user "+userCounter+" finished. WorkTime " +workLength + " sec \n");
            jButton2.setText("Начать работу");
            jTextArea1.append("Current conversia: " + ((double)userSuccessCounter/userCounter*100) +"% \n");
            MyReport.AddRecord(userCounter, workLength, false);            
            jPanel1.setVisible(false);
        }
    }//GEN-LAST:event_jButton2MouseClicked

