    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if (flag){
            finishTime=System.currentTimeMillis();
            workLength=(double)(finishTime-startTime)/1000;
            jTextArea1.append("user "+userCounter+"successfully finished. WorkTime " +workLength + " sec \n");
            jButton2.setText("Начать работу");
            MyReport.AddRecord(userCounter, workLength, true);
            userSuccessCounter ++;
            jTextArea1.append("Current conversia: " + ((double)userSuccessCounter/userCounter*100) +"% \n");
            flag = false;
            jPanel1.setVisible(false);
        }
    }//GEN-LAST:event_jButton1MouseClicked

